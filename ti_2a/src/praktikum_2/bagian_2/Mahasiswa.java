package praktikum_2.bagian_2;

 class Mahasiswa {
    // atribut private
    private String nama;
    private int umur;

    // getter dan setter
    public String getnama() {
        return nama;
    }
    public void setnama(String nama) {
        this.nama = nama;
    }
    public int getumur() {
        return umur;
    }
    public void setumur(int umur) {
        this.umur = umur;
    }
}
