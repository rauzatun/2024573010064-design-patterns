package praktikum_3.bagian_2;

 class Mahasiswa {
    // atribut private
    private String nama;
    private String umur;

    // getter dan setter
    public String getnama() {
        return nama;
    }
    public void setnama(String nama) {
        this.nama = nama;
    }
    public String getumur() {
        return umur;
    }
    public void setumur(String umur) {
        this.umur = umur;
    }
}
